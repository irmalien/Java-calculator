    private void PogaAprekins (String aprekins){        
        String lauks1temp = Lauks1.getText();
        String lauks2temp = Lauks2.getText();
        Aprekins aprekins2 = new Aprekins();
        String aprekinsTemp = "";
        boolean repeatedCalculation = false;
        
        if (lauks1temp.equals("0") || 
            lauks1temp.equals("+") || 
            lauks1temp.equals("-") || 
            lauks1temp.equals("x") || 
            lauks1temp.equals("^") || 
            lauks1temp.equals("/") || 
            lauks1temp.equals("%")){
            Lauks1.setText("");
            darbibaStringOld = aprekins;
            darbibaString = aprekins;
            repeatedCalculation = true;
        }
        else {
        darbibaStringOld = darbibaString; //saglabājam pagājušo matematisko funkciju
        darbibaString = aprekins; //jaunā aprēķina funkcija, kas tiek iedarbināta
        }
        
        //If Field1 equals nothing, then just save number from Field1 to Num1
        if(lauks2temp.equals(""))
            Lauks2.setText(lauks1temp);
        
        else if(repeatedCalculation == true){
            
        }

        else{
            double num1Int = Double.valueOf(lauks2temp); // pagājušo skaitļu summa
            double num2Int = Double.valueOf(lauks1temp); //jaunais skaitlis
            aprekinsTemp = aprekins2.aprekinsAll(darbibaStringOld, num1Int, num2Int);
            Lauks2.setText(aprekinsTemp);            
        }
        darbiba = true;
        Lauks1.setText(darbibaString);
        //reģistrē funkciju 
    }
    
    
