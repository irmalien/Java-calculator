    private void Poga_equalsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Poga_equalsActionPerformed
        // Read fields and save result as variables:
        String lauks1temp = Lauks1.getText();
        String lauks2temp = Lauks2.getText();
        
        // If field1 contains anything but number   
        Aprekins aprekins = new Aprekins();
        if (lauks1temp.equals("+") || 
            lauks1temp.equals("-") || 
            lauks1temp.equals("x") || 
            lauks1temp.equals("^") || 
            lauks1temp.equals("/") || 
            lauks1temp.equals("%")){
            Lauks1.setText(lauks2temp);
            Lauks2.setText("");
            darbiba = false;
            darbibaString = "";
        }
        // If field2 is empty (i.e. first calculation)
        else if (lauks2temp.equals("")){
            Lauks1.setText(lauks1temp);
            darbiba = false;
            darbibaString = "";
        }
        // Perform calculation and read result
        else{
            double num1Int = Double.valueOf(lauks2temp);
            double num2Int = Double.valueOf(lauks1temp);
            Lauks1.setText(aprekins.aprekinsAll(darbibaString, num1Int, num2Int));
            Lauks2.setText("");
            darbiba = false;
            darbibaString = "";
        }
    }//GEN-LAST:event_Poga_equalsActionPerformed

